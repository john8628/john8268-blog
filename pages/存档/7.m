我害怕，有一天别人或自己把自己捧的很高，然后我却开始忘乎所以；

我害怕，自己出生在农村，从小就放牛，在田野里长大的孩子，忘记了乡土的味道；在都市的弥红灯下，忘记了出发点，也忘记了要去哪里？

我害怕，一天父母老去，自己却不知道怎么去照顾他们，呵护它们，不懂得它们的语言，不懂得它们的需要，自己变得对爱的麻木不仁；

我害怕，我想给的，不是亲爱的想要的；害怕我们的沟通不够，让自己变的狭隘和很蠢的方式，去面对爱情；

我害怕自己不够智慧，忘记了自己能力的有限，而不断盲目去攀登；摔掉了腿，而忘记了生命的真正意义在哪里？

我害怕，我自己老去了，但是世界却没有留下 我的任何痕迹；而我的死亡，留给了世界最后一抔土，成了唯一且最后的贡献；

我们终将老去，不要觉得，战胜谁，比较重要，做一个智慧的人，更重要；认识自己，改变自己最重要；

学习，让自己的内心敬畏，谦卑。因为世界太伟大，让自己太无知。